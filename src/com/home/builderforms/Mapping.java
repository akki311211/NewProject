package com.appnetix.app.util.form;

import java.util.*;

public class Mapping
{
	private String  thisField;
	private String  parentField;
	private String  compField;
	private String  perField;
	private String  calc;
	private String  methodExecuted;
		

	public void setthisField(String value)
	{
		this.thisField = value;
	}
	public String getthisField()
	{
		return thisField;
	}

	public void setparentField(String value)
	{
		this.parentField = value;
	}
	public String getparentField()
	{
		return parentField;
	}

	public void setcompField(String value)
	{
		this.compField = value;
	}
	public String getcompField()
	{
		return compField;
	}

	public void setperField(String value)
	{
		this.perField = value;
	}
	public String getperField()
	{
		return perField;
	}

	public void setmethodExecuted(String value)
	{
		this.methodExecuted = value;
	}
	public String getmethodExecuted()
	{
		return methodExecuted;
	}

	public void setcalc(String value)
	{
		this.calc = value;
	}
	public String getcalc()
	{
		return calc;
	}

	
}